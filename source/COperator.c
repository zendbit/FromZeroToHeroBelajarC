#include "COperator.h"

void contohOperatorMath() {
  int a, b;
  a = 30;
  b = 20;
  printf("nilai a = %d, b = %d\n", a, b);
  printf("a + b = %d\n", (a + b));
  printf("a - b = %d\n", (a - b));
  printf("a / b = %f\n", ((float)a / (float)b));
  printf("a * b = %d\n", (a * b));
  printf("a %% b = %d\n", (a % b));
  printf("======================\n");
}

void contohOperatorUnary() {
  int a, b;
  a = b = 2;
  printf("nilai a = %d, b = %d\n", a, b);
  printf("a++: %d\n", a++);
  printf("nilai setelah post increment a: %d\n", a);
  printf("++b: %d\n", ++b);
  printf("nilai setelah pre b: %d\n", b);
  printf("======================\n");
  
  int x, y;
  a = b = 2;
  x = a++;
  y = ++b;
  printf("x: %d\n", x);
  printf("y: %d\n", y);
  printf("======================\n");
}

void contohOperatorRelationalLogical() {
  int a, b;
  a = 15;
  b = 20;
  printf("nilai a = %d, b = %d\n", a, b);
  printf("a > b: %d\n", (a > b));
  printf("a < b: %d\n", (a < b));
  printf("a >= b: %d\n", (a >= b));
  printf("a <= b: %d\n", (a <= b));
  printf("a != b: %d\n", (a != b));
  printf("======================\n");
}

void contohOperatorLogical() {
  int a, b;
  a = 2;
  b = 5;

  printf("nilai a = %d, b = %d\n", a, b);
  printf("a < b && a > b: %d\n", ((a < b) && (a > b)));
  printf("a < b || a > b: %d\n", ((a < b) || (a > b)));
  printf("!(a < b): %d\n", !(a < b));
  printf("======================\n");
}

void contohOperatorBitwise() {
  int a, b;
  a = 10;
  b = 1;

  printf("nilai a = %d, b = %d\n", a, b);
  printf("a & b: %d\n", (a & b));
  printf("a | b: %d\n", (a | b));
  printf("a ^ b: %d\n", (a ^ b));
  printf("~a: %d\n", ~a);
  printf("a >> b: %d\n", (a >> b));
  printf("a << b: %d\n", (a << b));
  printf("======================\n");
}

